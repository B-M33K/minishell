#include "libft.h"

void	ft_putstrstr_fd(char **str, int fd)
{
	int	i;

	i = 0;
	while (str[i])
	{
		ft_putstr_fd(str[i], fd);
		ft_putendl_fd("%", fd);
		i++;
	}
}
