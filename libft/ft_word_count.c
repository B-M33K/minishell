#include "libft.h"

int	ft_word_count(const char *s, char c)
{
	int	wc;
	int	i;

	wc = 0;
	i = 0;
	if (s)
	{
		if (*(s + i) && *(s + i) != c)
			wc++;
		while (*(s + i))
		{
			if (*(s + i) == c && *(s + i + 1) != c && *(s + i + 1))
				wc++;
			i++;
		}
	}
	return (wc);
}
