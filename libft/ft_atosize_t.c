#include "libft.h"

size_t	ft_atosize_t(char *str)
{
	size_t	n;
	int		sign;

	if (*str == '+' || *str == '-')
	{
		if (*str == '-' )
			sign = -1;
		str++;
	}
	n = 0;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	while (*str >= '0' && *str <= '9')
	{
		n = n * 10 + (*str - '0');
		str++;
	}
	return (n);
}
